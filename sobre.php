	<p class="historia" style="margin-top:40px">A história da Realce Móveis começou quando seu fundador Carlos Eduardo Chagas trabalhava na em outra loja com instalação de divisórias, e a cada dois ou três meses ele queimava várias sobras de material. 
	Porem sempre que queimava as sobras ele refletia <i>“isto é dinheiro, posso reaproveitar esse material para alguma coisa”</i>. Com uma ideia na mente e a vontade de montar seu próprio negocio ele decidiu que havia chegado a hora de 
	colocar aquilo em pratica e montar balcões de divisória. Então em um sábado Carlos conhecido como Kako começou a montar seu primeiro balcão, porem ele nunca teve nenhuma ferramenta, então naquele dia a cada necessidade ele corria
	na loja comprar a ferramenta necessária no momento para desenvolver o projeto.</p> 
	<p class="historia">Seu primeiro balcão não ficou muito bom porem com o tempo ele se aprimorou e se tornou pioneiro nesse ramo na cidade além de ser o pioneiro também em reforma de moveis de aço o que deu origem ao nome da loja 
	Realce Móveis, que a principio tinha como principal prestação de serviço estas restaurações.</p> 
	<p class="historia">Porem ainda faltava uma coisa para deixar o sonho completo, Kako queria que sua esposa Célia trabalhasse com ele, porem o barracão onde hoje se encontra a loja era apenas usado como depósito.</p>
	<p class="historia">Então um dia Célia disse <i>“monte uma loja que eu vou trabalhar com você”</i>, no dia seguinte começou a reforma do barracão, que se transformou na loja Realce Móveis e Célia passou a tomar conta da parte administrativa
	fazendo com que o serviço ficasse mais otimizado.</p>
	<p class="historia" style="margin-bottom: 40px">Hoje em dia a loja Realce Móveis vende vários produtos para casa e escritório e presta vários serviço como instalação de divisórias, forros, pisos entre outros.</p>
 
	
